	 <p>To help safeguard law enforcement officers, emergency responders, road maintenance workers, and others who work on the side of highways, Wisconsin has a "Move Over Law."</p>
	 <h4  style="color:#f8bf4c">Drivers must provide a safety zone for stopped law enforcement, emergency and maintenance vehicles.</h4>
	 <p>The law requires drivers to shift lanes or slow down in order to provide a "safety zone" for a squad car, ambulance, fire truck, tow truck or highway maintenance vehicle that is stopped on the side of a road with its warning lights flashing.</p>
	 <h4  style="color:#f8bf4c">Drivers have two options for creating a safety zone:</h4>
	 <p> &middot; If the road has more than one directional lane, like the Interstate, and you can switch lanes safely, you must move over to vacate the lane closest to the law enforcement or other vehicle with its lights flashing.</p>
	 <p> &middot; If the road has a single directional lane or you can't safely move over, you must reduce your speed.
</p>
	 <p>Failure of motorists to move over is one of the reasons that motor vehicle crashes kill more law enforcement officers on duty than any other cause.</p>
